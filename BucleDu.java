public class BucleDu {
    public static void main(String[] args) {
        //Do While
        /**
         * do { 
         *  --instrucciones
         * }while(--condicion);
         */
        
        int i = 1000;
        do{
            System.out.println(i+", ");
            i-=200;
        }while(i>=0);
    }
}
